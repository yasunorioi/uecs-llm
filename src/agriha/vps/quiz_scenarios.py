def get_random_quiz() -> str:
    """ランダムに1問選んで出題テキストを返す"""
    s = random.choice(SCENARIOS)
    difficulty_emoji = {"Easy": "🟢", "Medium": "🟡", "Hard": "🔴"}
    emoji = difficulty_emoji.get(s["difficulty"], "")

    text = (
        f"📝 問題{s['id']} {emoji}{s['difficulty']}\n"
        f"「{s['title']}」\n"
        f"（{s['date']}）\n"
        f"\n"
        f"【状況】\n"
        f"{s['situation']}\n"
        f"\n"
        f"【データ抜粋】\n"
        f"{s['data_summary']}\n"
        f"\n"
        f"【質問】\n"
        f"{s['questions']}\n"
        f"\n"
        f"💡 回答を送信すると、AIも同じ問題に答えます。"
        f"あなたとAIの判断を比較してみましょう！"
    )
    return text
